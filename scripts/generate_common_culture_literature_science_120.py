def rotate_answer_positions(questions: list[dict]) -> None:
    """Spread correct answers across A/B/C/D without changing question meaning."""
    letters = ["A", "B", "C", "D"]
    option_keys = ["option_a", "option_b", "option_c", "option_d"]

    for index, item in enumerate(questions):
        if item["answer"] != "A":
            continue

        target_index = index % len(option_keys)
        if target_index == 0:
            continue

        options = [item[key] for key in option_keys]
        correct_option = options.pop(0)
        options.insert(target_index, correct_option)

        for key, value in zip(option_keys, options):
            item[key] = value
        item["answer"] = letters[target_index]


def main() -> None:
    batch = QUESTIONS[:120]
    if len(batch) != 120:
        raise RuntimeError(f"Expected 120 questions, got {len(batch)}")
    rotate_answer_positions(batch)
    OUTPUT_PATH.write_text(json.dumps({"questions": batch}, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(batch)} questions to {OUTPUT_PATH.relative_to(PROJECT_ROOT)}")
    print(f"Reserved {len(QUESTIONS) - len(batch)} extra questions for a later batch")


if __name__ == "__main__":
    main()
