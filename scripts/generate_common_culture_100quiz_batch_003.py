def rotate_answer_positions(questions: list[dict]) -> None:
    letters = ["A", "B", "C", "D"]
    option_keys = ["option_a", "option_b", "option_c", "option_d"]
    for index, item in enumerate(questions):
        target_index = index % len(option_keys)
        if target_index == 0:
            continue
        options = [item[key] for key in option_keys]
        correct_option = options.pop(0)
        options.insert(target_index, correct_option)
        for key, value in zip(option_keys, options):
            item[key] = value
        item["answer"] = letters[target_index]


def existing_stems() -> set[str]:
    stems: set[str] = set()
    for path in (PROJECT_ROOT / "data").glob("*.json"):
        if path == OUTPUT_PATH:
            continue
        try:
            payload = json.loads(path.read_text(encoding="utf-8"))
        except Exception:
            continue
        questions = payload.get("questions") if isinstance(payload, dict) else payload
        if not isinstance(questions, list):
            continue
        for item in questions:
            if isinstance(item, dict) and isinstance(item.get("stem"), str):
                stems.add(item["stem"].strip())
    return stems


def main() -> None:
    if len(QUESTIONS) != 100:
        raise RuntimeError(f"Expected 100 questions, got {len(QUESTIONS)}")
    repeated = sorted({item["stem"] for item in QUESTIONS if item["stem"].strip() in existing_stems()})
    if repeated:
        raise RuntimeError("Duplicate stems found in local data files:\n" + "\n".join(repeated[:20]))
    rotate_answer_positions(QUESTIONS)
    OUTPUT_PATH.write_text(json.dumps({"questions": QUESTIONS}, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(QUESTIONS)} questions to {OUTPUT_PATH.relative_to(PROJECT_ROOT)}")


if __name__ == "__main__":
    main()
