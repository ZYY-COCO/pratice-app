def build_questions() -> list[dict]:
    questions: list[dict] = []
    for index, (module, submodule, concept, clue, distractors, wrong_clues, difficulty) in enumerate(CARDS):
        questions.append(
            make_question(
                module,
                submodule,
                f"综合中华文化材料复习中，哪一项最符合“{clue}”？",
                [concept, *distractors],
                f"{concept}：{clue}。",
                difficulty,
                index,
            )
        )
        questions.append(
            make_question(
                module,
                submodule,
                f"依据中华文化考纲，关于“{concept}”的判断正确的是：",
                [clue, *wrong_clues],
                f"“{concept}”对应的核心知识点是：{clue}。",
                difficulty,
                index + 1,
            )
        )

    stems = [item["stem"] for item in questions]
    repeated = sorted({stem for stem in stems if stems.count(stem) > 1})
    if repeated:
        raise RuntimeError("Duplicate stems inside generated batch:\n" + "\n".join(repeated))

    return questions


def main() -> int:
    questions = build_questions()
    payload = {"questions": questions}
    OUTPUT_PATH.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(questions)} questions to {OUTPUT_PATH.relative_to(PROJECT_ROOT)}")

    by_module: dict[str, int] = {}
    for item in questions:
        by_module[item["module"]] = by_module.get(item["module"], 0) + 1
    for module, count in by_module.items():
        print(f"  {module}: {count}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
