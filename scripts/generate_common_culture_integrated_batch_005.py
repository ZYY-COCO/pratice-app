def rotate_options(options: list[str], correct_index: int) -> tuple[dict[str, str], str]:
    letters = ["A", "B", "C", "D"]
    rotated = options[correct_index:] + options[:correct_index]
    correct_value = options[0]
    mapped = dict(zip(letters, rotated))
    answer = next(letter for letter, value in mapped.items() if value == correct_value)
    return mapped, answer


def make_question(
    module: str,
    submodule: str,
    stem: str,
    options: list[str],
    explanation: str,
    difficulty: int,
    answer_position: int,
) -> dict:
    mapped, answer = rotate_options(options, answer_position % 4)
    return {
        "exam_code": "COMMON",
        "subject": "中华文化",
        "module": module,
        "submodule": submodule,
        "question_type": "single_choice",
        "stem": stem,
        "option_a": mapped["A"],
        "option_b": mapped["B"],
        "option_c": mapped["C"],
        "option_d": mapped["D"],
        "answer": answer,
        "explanation": explanation,
        "difficulty": difficulty,
        "source_type": "source_extracted",
        "source_year": 2026,
        "passage_id": None,
    }


def card(
    module: str,
    submodule: str,
    concept: str,
    clue: str,
    distractors: list[str],
    wrong_clues: list[str],
    difficulty: int,
) -> tuple[str, str, str, str, list[str], list[str], int]:
    if len(distractors) != 3 or len(wrong_clues) != 3:
        raise ValueError(f"Need 3 distractors and 3 wrong clues for {concept}")
    return module, submodule, concept, clue, distractors, wrong_clues, difficulty


